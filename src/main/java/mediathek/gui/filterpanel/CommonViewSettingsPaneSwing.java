    public void selectFilter(FilterDTO filter) {
        ComboBoxModel <FilterDTO> selectionModel = filterSelect.getModel();
        if (!filter.equals(selectionModel.getSelectedItem())) {
            selectionModel.setSelectedItem(filter);
        }
    }

